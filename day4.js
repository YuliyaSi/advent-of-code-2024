function countWordOccurrences(grid, word) {
    const rows = grid.length;
    const cols = grid[0].length;
    const wordLength = word.length;
    let count = 0;

    const directions = [
        [0, 1],   // Horizontal (right)
        [0, -1],  // Horizontal (left)
        [1, 0],   // Vertical (down)
        [-1, 0],  // Vertical (up)
        [1, 1],   // Diagonal (down-right)
        [-1, -1], // Diagonal (up-left)
        [1, -1],  // Diagonal (down-left)
        [-1, 1]   // Diagonal (up-right)
    ];


    function checkWord(row, col, dir) {
        for (let i = 0; i < wordLength; i++) {
            const newRow = row + i * dir[0];
            const newCol = col + i * dir[1];
            if (
                newRow < 0 || newRow >= rows || // Out of bounds
                newCol < 0 || newCol >= cols || // Out of bounds
                grid[newRow][newCol] !== word[i] // Character mismatch
            ) {
                return false;
            }
        }
        return true;
    }


    for (let row = 0; row < rows; row++) {
        for (let col = 0; col < cols; col++) {

            for (const dir of directions) {
                if (checkWord(row, col, dir)) {
                    count++;
                }
            }
        }
    }

    return count;
}

