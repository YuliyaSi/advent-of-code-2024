function countXMAS(grid) {
    const rows = grid.length;
    const cols = grid[0].length;
    const target1 = "MAS"; // Forward "MAS"
    const target2 = "SAM"; // Reverse "MAS"
    let count = 0;

    function checkDiagonals(centerRow, centerCol) {
        // Get diagonal characters
        const topLeft = centerRow - 1 >= 0 && centerCol - 1 >= 0 ? grid[centerRow - 1][centerCol - 1] : null;
        const bottomRight = centerRow + 1 < rows && centerCol + 1 < cols ? grid[centerRow + 1][centerCol + 1] : null;
        const topRight = centerRow - 1 >= 0 && centerCol + 1 < cols ? grid[centerRow - 1][centerCol + 1] : null;
        const bottomLeft = centerRow + 1 < rows && centerCol - 1 >= 0 ? grid[centerRow + 1][centerCol - 1] : null;
        const center = grid[centerRow][centerCol];

        const diag1 = `${topLeft || ''}${center}${bottomRight || ''}`; // Top-left to bottom-right
        const diag2 = `${topRight || ''}${center}${bottomLeft || ''}`; // Top-right to bottom-left

        return (
            (diag1 === target1 || diag1 === target2) &&
            (diag2 === target1 || diag2 === target2)
        );
    }

    // each cell as the center of an X
    for (let row = 0; row < rows; row++) {
        for (let col = 0; col < cols; col++) {
            if (checkDiagonals(row, col)) {
                count++;
            }
        }
    }

    return count;
}

